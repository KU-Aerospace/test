%-------------------------------------------------------------------------
function [coef_diff_vec] = L6_Poly_Diff_Coef(N,coef_vec)
%-------------------------------------------------------------------------
    for J = 1:N
        Jp = J+1  ;
        coef_diff_vec(J) = J*coef_vec(Jp) ;
    end
    coef_diff_vec(N+1) = 0.0 ;
%-------------------------------------------------------------------------
end
