%-------------------------------------------------------------------------
% Legdre polynomial and its derivative
%-------------------------------------------------------------------------
function [tau_vec,Weight_vec] = B1_Legedre_Gauss_Quadrature(N)
%-------------------------------------------------------------------------
%  (1) Quadrature Nodes
%-------------------------------------------------------------------------
   [tau_vec] = L2_Legendre_Poly_Roots(N) ;
%-------------------------------------------------------------------------
%  (2) Quadrature Weights
%-------------------------------------------------------------------------
%  (2-1) Polynomial Expansion of Lagrange Polymonials
%-------------------------------------------------------------------------
   [c_mat] = L5_Lagrange_Poly_Expansion(N,tau_vec) ;
%-------------------------------------------------------------------------
%  (2-2) Integration of Lagrange Polymonials
%-------------------------------------------------------------------------
   for J=1:N
       cvec(1:N) = c_mat(1:N,J) ;
%
       [cintg_vec] = L6_Poly_Intg_Coef(N-1,cvec) ; % (N)-th order
%
       taup =  1.0 ;
       [funp] = L6_Poly_Computing(N,cintg_vec,taup) ;
%
       taum = -1.0 ;
       [funm] = L6_Poly_Computing(N,cintg_vec,taum) ;
%
       Weight_vec(J) = funp - funm ;
   end
%-------------------------------------------------------------------------
end   
