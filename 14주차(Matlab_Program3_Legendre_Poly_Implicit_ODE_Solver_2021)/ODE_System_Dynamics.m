%-------------------------------------------------------------------------
function [dot_y] = ODE_System_Dynamics(y,t,T)
%-------------------------------------------------------------------------
   v_r        =  y(1)     ;
   v_theta    =  y(2)     ;
   r          =  y(3)     ;
   theta      =  y(4)     ;
%
   r2         = r*r       ;
%-------------------------------------------------------------------------
   Mue_E = 3.98600435608E+05 ;
%---------------------------------------------------------------
%  forces
%---------------------------------------------------------------
   force1 =  v_theta*v_theta/r - Mue_E/r2     ;
   force2 = -v_r*v_theta/r                     ;
   force3 =  v_r             ;
   force4 =  v_theta/r       ;
%-------------------------------------------------------------------------- 
%
%-------------------------------------------------------------------------
   dot_y(1) =  force1*T                 ;
   dot_y(2) =  force2*T                 ;
   dot_y(3) =  force3*T                 ;
   dot_y(4) =  force4*T                 ;
%-------------------------------------------------------------------------- 
end
%-------------------------------------------------------------------------

