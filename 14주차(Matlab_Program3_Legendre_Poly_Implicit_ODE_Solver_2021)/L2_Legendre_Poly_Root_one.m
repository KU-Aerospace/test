%-------------------------------------------------------------------------
% Legdre polynomial and its derivative
%-------------------------------------------------------------------------
function [tau] = L2_Legendre_Poly_Root_one(N,tau)
%-------------------------------------------------------------------------
%  (1) Parameter
%-------------------------------------------------------------------------
   Iter_max = 20      ;
   ToL_max  = 1.0E-10 ; 
%-------------------------------------------------------------------------
%  (2) Newton_Raphson Method
%-------------------------------------------------------------------------
   for iter = 1:Iter_max  
%-------------------------------------------------------------------------
      [fL,dfL] = L1_Legendre_Poly(N,tau) ;
      dtau = fL/dfL                      ;
      tau  = tau - dtau                  ;
      if(abs(dtau) <= ToL_max) 
          break                          ;
      end
%-------------------------------------------------------------------------
   end      
%-------------------------------------------------------------------------
end   
