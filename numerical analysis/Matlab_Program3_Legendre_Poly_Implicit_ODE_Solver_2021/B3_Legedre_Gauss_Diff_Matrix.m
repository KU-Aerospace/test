%-------------------------------------------------------------------------
% Legdre polynomial and its derivative
%-------------------------------------------------------------------------
function [tau_vec,Fdiff_mat] = B3_Legedre_Gauss_Diff_Matrix(N)
%-------------------------------------------------------------------------
%  (1) Quadrature Nodes
%-------------------------------------------------------------------------
   [tau_vec] = L2_Legendre_Poly_Roots(N) ;
%-------------------------------------------------------------------------
%  (2) Integration Matrix
%-------------------------------------------------------------------------
%  (2-1) Polynomial Expansion of Lagrange Polymonials
%-------------------------------------------------------------------------
   [c_mat] = L5_Lagrange_Poly_Expansion(N,tau_vec) ;
%-------------------------------------------------------------------------
%  (2-2) Integration of Lagrange Polymonials
%-------------------------------------------------------------------------
   for J=1:N
       cvec(1:N) = c_mat(1:N,J) ;
%
       [cdiff_vec] = L6_Poly_Diff_Coef(N-1,cvec) ; % (N-2)-th order
%
       taum = -1.0 ;
       [funm] = L6_Poly_Computing(N-2,cdiff_vec,taum) ;
%
       for K=1:N
           taup =  tau_vec(K) ;
           [funp] = L6_Poly_Computing(N-2,cdiff_vec,taup) ;
%
           Fdiff_mat(K,J) = funp - funm ;
       end
   end
%-------------------------------------------------------------------------
end   
