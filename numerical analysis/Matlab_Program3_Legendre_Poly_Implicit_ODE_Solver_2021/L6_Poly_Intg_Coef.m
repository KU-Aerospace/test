function [coef_intg_vec] = L6_Poly_Intg_Coef(N,coef_vec)
%-------------------------------------------------------------------------
    coef_intg_vec(1) = 0.0         ;
    coef_intg_vec(2) = coef_vec(1) ;
    for J = 2:N+1
        Jp = J+1  ;
        coef_intg_vec(Jp) = coef_vec(J)/J ;
    end
%-------------------------------------------------------------------------
end
