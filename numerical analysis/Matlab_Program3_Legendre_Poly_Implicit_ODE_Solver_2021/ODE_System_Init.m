%-------------------------------------------------------------------------
function [y0,Nstep,taumax,T] = ODE_System_Init(eccen)
%-------------------------------------------------------------------------
  Mue_E = 3.98600435608E+05 ;
  r_E   = 6378.145              ; % Earth's radius in km
  r_P   = r_E + 100.0           ; % Perigee radius
  P     = (1.0+eccen)*r_P       ; % orbital constant p
  AA    = P/(1.0-eccen^2)       ; % semi major axis
%
  T  = 2.0*pi*AA*sqrt(AA/Mue_E) ; % Orbit period
%-------------------------------------------------------------------------
% (1) Initial condition
%-------------------------------------------------------------------------
  y0(1) = 0.0                 ; % v_r
  y0(2) = sqrt(P*Mue_E)/r_P   ; % v_theta  
  y0(3) = r_P                 ; % r
  y0(4) = 0.0                 ; % theta
%-------------------------------------------------------------------------
% (2) Maximum Time Horizon and Time Step Size
%-------------------------------------------------------------------------
  No_rev    = 10               ;
  Nper_rev  = 100              ;
  Nstep     = No_rev*Nper_rev + 1 ;
%
  taumax    = No_rev*1.0          ;
%-------------------------------------------------------------------------
end
%-------------------------------------------------------------------------

