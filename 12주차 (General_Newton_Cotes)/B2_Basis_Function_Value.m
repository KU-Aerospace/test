%----------------------------------------
%  Basis Function Coefficients
%----------------------------------------
function [Fun] = B2_Basis_Function_Value(K,T,Coef)
%----------------------------------------
% (1) Initialize
%----------------------------------------
     Fun = Coef(K+1) ;
%----------------------------------------
% (1) Recursive computing
%----------------------------------------
     for j = K:-1:1
         Fun = Fun*T + Coef(j) ;
     end
%----------------------------------------
end
