%----------------------------------------
%  Basis Function Coefficients
%----------------------------------------
function [Beta] = B1_Basis_Function_Coef(N,Tau)
%----------------------------------------
% (1) Initialize
%----------------------------------------
     Beta(1:N,1:N-1) = 0.0 ;
%----------------------------------------
% (1) g1- function
%----------------------------------------
      Beta(1,1) = 0.0 ;
      Beta(2,1) = 1.0 ;
%----------------------------------------
% (2) g2, g3, g4,   gn - function
%----------------------------------------
    for k  = 2:N-1
        km = k-1    ;
        T1 = Tau(k) ;
        Beta(1,k) = -Beta(1,km)*T1  ;
        for m=2:k
            L = m-1 ;
            Beta(m,k) = Beta(L,km) - Beta(m,km)*T1  ;
        end
        Beta(k+1,k) = 1.0;
    end
%----------------------------------------
end
