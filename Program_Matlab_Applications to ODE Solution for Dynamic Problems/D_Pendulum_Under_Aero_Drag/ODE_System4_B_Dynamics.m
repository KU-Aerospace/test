%-------------------------------------------------------------------------
function [dot_y] = ODE_System4_B_Dynamics(y,t)
%-------------------------------------------------------------------------
   theta    =  y(1)  ; % angle
   dtheta   =  y(2)  ; % angular velocity
%-------------------------------------------------------------------------
   Length  = 1.0    ;
   CD      = 0.50   ;
   Density = 1.2250 ;
   Mass    = 0.50   ;
   Radius  = 0.1    ;
   GravCoef= 9.8    ;
   Area    = 0.75 ;
%
   Coef1 = -GravCoef/Length     ;
   Coef2 = -CD*Density*Area*0.5/Mass ;
%------------------------------------------------------------------------
   alpa =  0.50;
   omega=  1.00;
%
   force   =  0      ;% force=f(t) should be defined
%------------------------------------------------------------------------
   dot_y(1) =  dtheta                                      ;
   dot_y(2) =  Coef1*sin(theta) + Coef2*dtheta*abs(dtheta) ;
%-------------------------------------------------------------------------
