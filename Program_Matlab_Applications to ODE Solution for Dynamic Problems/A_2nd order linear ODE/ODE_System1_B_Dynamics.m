function [dot_y] = ODE_System1_B_Dynamics(y,t)
%-------------------------------------------------------------------------
   y1    =  y(1)     ;
   y2    =  y(2)     ;
%-------------------------------------------------------------------------
   a0    = 16.0      ;
   a1    =  2.0      ;
%
   force =0.1*cos(t) ;
%-------------------------------------------------------------------------
   dot_y(1) =  y(2)                  ;
   dot_y(2) =  force - a0*y1 - a1*y2 ;
%-------------------------------------------------------------------------
